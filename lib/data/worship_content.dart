class Dhikr {
  final String text;
  final int repeat; // عدد التكرار
  final String? note; // فضل الذكر أو تنبيه (اختياري)
  const Dhikr(this.text, {this.repeat = 1, this.note});
}

class Faidah {
  final String kind; // 'آية' | 'حديث'
  final String text; // نص الآية/الحديث
  final String explanation; // التفسير/الشرح المختصر
  final String source; // المرجع
  const Faidah({
    required this.kind,
    required this.text,
    required this.explanation,
    required this.source,
  });
}

